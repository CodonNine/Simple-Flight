void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    static uint8_t tick = 0;
    if (htim->Instance == TIM6)
    {
        tick++;
        if (tick == 5)
        {
            button_ticks();
            tick = 0;
        }
    }
}

void HAL_UART_AbortReceiveCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART1)
    {
        U1_RX_Struct.RX_In->end = &U1_RX_Buffer[U1_RX_Struct.RX_Counter - 1];
        U1_RX_Struct.RX_In++;
        if (U1_RX_Struct.RX_In == U1_RX_Struct.RX_End)
            U1_RX_Struct.RX_In = &U1_RX_Struct.SE_Buffer[0];
        if ((RX_BUFFER_SIZE - U1_RX_Struct.RX_Counter) < RX_MAX)
        {
            U1_RX_Struct.RX_Counter = 0;
            U1_RX_Struct.RX_In->start = &U1_RX_Buffer[0];
        } else
        {
            U1_RX_Struct.RX_In->start = &U1_RX_Buffer[U1_RX_Struct.RX_Counter];
        }
        HAL_UART_Receive_IT(&huart1, U1_RX_Struct.RX_In->start, RX_MAX);
    }

    if (huart->Instance == USART2)
    {
        U2_RX_Struct.RX_In->end = &U2_RX_Buffer[U2_RX_Struct.RX_Counter - 1];
        U2_RX_Struct.RX_In++;
        if (U2_RX_Struct.RX_In == U2_RX_Struct.RX_End)
            U2_RX_Struct.RX_In = &U2_RX_Struct.SE_Buffer[0];
        if ((RX_BUFFER_SIZE - U2_RX_Struct.RX_Counter) < RX_MAX)
        {
            U2_RX_Struct.RX_Counter = 0;
            U2_RX_Struct.RX_In->start = &U2_RX_Buffer[0];
        } else
        {
            U2_RX_Struct.RX_In->start = &U2_RX_Buffer[U2_RX_Struct.RX_Counter];
        }
        HAL_UART_Receive_IT(&huart2, U2_RX_Struct.RX_In->start, RX_MAX);
    }
}
